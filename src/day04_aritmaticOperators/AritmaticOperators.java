package day04_aritmaticOperators;

public class AritmaticOperators {

	public static void main(String[] args) {
		
		// Addition Operators +
		
		System.out.println(55+5);
		int chairs=3;
		System.out.println(chairs+120);
		
		int tables=130;
		int total=tables+chairs;
		System.out.println(total);
		
		//Subtraction -
		double balance=12000.50;
		double transaction=56.44;
		System.out.println(balance-transaction);
		balance=balance-transaction;
		System.out.println(balance);
		//example
		int LineOfCode=50;
		System.out.println("Lines of Code "+LineOfCode);
		
		LineOfCode=LineOfCode-2;
		
		System.out.println("Lines of Code "+LineOfCode);
		
		//Multiplication
		
		int clases=5;
		System.out.println(LineOfCode*clases);
		
		//Division
		System.out.println(10/2);
		
		//Remainder % kalan kismi gosterir
		System.out.println(10%2);
		
		
				
		
	}

}
