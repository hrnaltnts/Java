package day10_TernaryOperator_For_Loop;

public class ForLoop {

	public static void main(String[] args) {
		
		for(int i=0 ; i<5 ; i++) {
			System.out.println("Hello");
			// i condition action incerement i++ 
			// 0  TRUE Print Hello    1
			//    TRUE print Hello   2
			//    TRUE print Hello   3
			//    TRUE print Hello   4
			//	  TRUE print Hello   5
			//    FALSE 
		}
		for(int num=20;num>=10;num--) {
			System.out.println("num="+num);
						//num=20
						//num=19
						//num=18
						//num=17
						//num=16
						//num=15
						//num=14
						//num=13
						//num=12
						//num=11
						//num=10
		}
		for(int a=0;a<=100;a+=10) {
				System.out.println("a= "+a);
				//a= 0
				//a= 10
				//a= 20
				//a= 30
				//...
				//a= 100
			}
		for(int b=50;b>=0;b-=5) {
			System.out.println("b="+b);
					//b=50
					//b=45
					//b=40
					//...
					//b=0

		}

			}
		}

	


