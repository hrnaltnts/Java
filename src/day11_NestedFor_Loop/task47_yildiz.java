package day11_NestedFor_Loop;

public class task47_yildiz {

	public static void main(String[] args) {
		
		int rows=7;
		int colums=6;
		
		for(int i =1; i<=rows; i++) {
			for(int j=1; j<=colums; j++) {
				System.out.print("*");
			}
			System.out.println();
		}

	}

}

